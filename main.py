from flask import Flask, render_template, request
import pickle
import numpy as np

# Initialize app
app = Flask(__name__)

# Load trained model
model = pickle.load(open("ckdp_rechanged.pkl", "rb"))

@app.route("/")
def home():
    return render_template("index.html")

@app.route("/predict", methods=["POST"])
def predict():
    try:
        # Get values from form
        features = [
            float(request.form["age"]),
            float(request.form["blood_pressure"]),
            float(request.form["specific_gravity"]),
            float(request.form["albumin"]),
            float(request.form["blood_urea"]),
            float(request.form["serum_creatinine"]),
            float(request.form["hemoglobin"]),
            float(request.form["packed_cell_volume"]),
            float(request.form["diabetes_mellitus"]),
            float(request.form["hypertension"]),
            float(request.form["red_blood_cell_count"]),
            float(request.form["blood_glucose_random"]),
        ]

        # Convert to array for model input
        final_input = np.array([features])
        prediction = model.predict(final_input)[0]

        result = "Chronic Kidney Disease Detected" if prediction == 1 else "No CKD Detected"
        return render_template("index.html", prediction_text=result)

    except Exception as e:
        return render_template("index.html", prediction_text="Error: " + str(e))

if __name__ == "__main__":
    app.run(debug=True)
